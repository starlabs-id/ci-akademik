<p><b>Reputasi</b></p>
<p><br>Sejak tahun 1978 STMIK Kian Santangtelah mendapat banyak reputasi, di antaranya yang terbaru adalah sebagai berikut.</p>
<p>Pada tahun 2009,STMIK Kian Santang mendapat sertifikasi ISO 9001:2000 untuk Proses Belajar Mengajar</p>
<p>Pada tahun 2007, STMIK Kian Santang Terakreditasi A dari BAN-PT (Badan Akreditasi Nasional – Perguruan Tinggi)</p>
<p>Pada tahun 2006, STMIK Kian Santang bekerja sama dengan APTECH World-Wide (Global IT Education), lembaga sertifikasi internasional yang berpusat di India, untuk menyelenggarakan pelatihan perangkat lunak bidang pemrograman, basis data, tools, jaringan dan internet.</p>
<p>Pada tahun 2007, STMIK Kian Santang diminta oleh LSP-Telematika (Lembaga Sertifikasi Profesi Telekomunikasi, Multimedia dan Informatika) agar berperan sebagai lokomotif dalam menyelenggarakan TUK (Tempat Uji Kompetensi). Dosen-dosen JTK juga mulai berperan sebagai asesor BNSP (Badan Nasional Sertifikasi Profesi) dan sebagai nara sumber pengadaan modul-modul pelatihan SKKNI (Standar Kompetensi Kerja Nasional Indonesia) dan sebagai reviewer bank-bank soal yang akan dijadikan sebagai MJK (Materi Uji Kompetensi) untuk sertifikasi profesi nasional.</p>
