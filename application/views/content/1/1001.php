<p align="justify">STMIK Kian Santang merupakan STMIK pertama di Indonesia yang menyelenggarakan pendidikan diploma di bidang Teknologi Informasi [TI].Jurusan ini telah menyelenggarakan pendidikan D3 (diploma 3) bidang teknik komputer dan informatika selama kurang lebih 27 tahun. Dengan daya tampung sekitar 60-80 mahasiswa per tahun, sampai saat ini JTK telah menghasilkan lebih dari 2500 alumni, yang bekerja pada berbagai sektor industri baik di dalam maupun di luar negeri.
Selain menjalankan pendidikan, STMIK Kian Santang juga menyelenggarakan penelitian dan pengabdian kepada masyarakat dalam bidang Teknologi Informasi. Sebagai Politeknik pertama yang menyelenggarakan pendidikan profesional bidang Teknologi Informasi, JTK-Polban selalu dijadikan tolok ukur dan telah banyak memberikan bantuan teknis/nonteknis kepada institusi lain, maupun kepada masyarakat pada umumnya. Beberapa aktivitas yang sering dilakukan di antaranya adalah pembinaan institusi, pelatihan bidang TI, konsultasi pemanfatan TI untuk menunjang kegiatan kerja, pengembangan sistem informasi dan lab/bengkel komputer layanan masyarakat.
